import java.io.*; 
  
class Assignment3q1 { 
    public static void main(String[] args) 
    { 
  
        // Declaration and Assignment 
        int t1 = 5, t2 = 15; 
  
        System.out.println("t1 = " + t1); 
        System.out.println("t2 = " + t2); 
  
        // Adding & Assigning values(Expressions) 
        t1 += t2; 
  
        // Displaying the assigned values 
        System.out.println("t1 = " + t1); 
    } 
} 